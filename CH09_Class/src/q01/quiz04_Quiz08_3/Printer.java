package q01.quiz04_Quiz08_3;

public class Printer {
	
	public void println(int value) {
		System.out.println(value);
	}
	
	public void println(boolean value) {
		System.out.println(value);
	}
	
	public void println(double value) {
		System.out.println(value);
	}
	
	public void println(String value) {
		System.out.println(value);
	}
}
